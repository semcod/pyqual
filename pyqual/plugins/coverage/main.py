"""Coverage plugin for pyqual — test coverage metrics.

Collects metrics from coverage.json generated by pytest-cov.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any

from pyqual.plugins import MetricCollector, PluginMetadata, PluginRegistry


@PluginRegistry.register
class CoverageCollector(MetricCollector):
    """Coverage metrics collector — extracts test coverage data."""

    name = "coverage"
    metadata = PluginMetadata(
        name="coverage",
        description="Test coverage metrics from pytest-cov",
        version="1.0.0",
        tags=["coverage", "testing", "pytest"],
        config_example="""
metrics:
  coverage_min: 80.0           # Minimum line coverage %
  coverage_branch_min: 70.0      # Minimum branch coverage %

stages:
  - name: test_coverage
    run: pytest --cov=pyqual --cov-report=json:.pyqual/coverage.json --cov-branch
""",
    )

    def collect(self, workdir: Path) -> dict[str, float]:
        """Collect coverage metrics from coverage.json."""
        result: dict[str, float] = {}
        
        cov_path = workdir / ".pyqual" / "coverage.json"
        if not cov_path.exists():
            cov_path = workdir / "coverage.json"
        
        if cov_path.exists():
            try:
                data = json.loads(cov_path.read_text())
                totals = data.get("totals", {})
                
                # Line coverage
                total_pct = totals.get("percent_covered")
                if total_pct is not None:
                    result["coverage"] = float(total_pct)
                
                # Branch coverage
                num_branches = totals.get("num_branches")
                covered_branches = totals.get("covered_branches")
                if num_branches and covered_branches is not None and num_branches > 0:
                    result["coverage_branch"] = (covered_branches / num_branches) * 100
                    
            except (json.JSONDecodeError, TypeError, KeyError):
                pass
        
        return result


def coverage_summary(workdir: Path | None = None) -> dict[str, Any]:
    """Generate coverage summary."""
    workdir = workdir or Path.cwd()
    collector = CoverageCollector()
    metrics = collector.collect(workdir)
    
    return {
        "success": True,
        "metrics": metrics,
        "line_coverage": metrics.get("coverage", 0.0),
        "branch_coverage": metrics.get("coverage_branch", 0.0),
    }
